<h3>Giving Options</h3>

There are many giving options available to contribute to Louie's Last Regatta and Children's Hospital of Wisconsin. All money raised goes to Children's Hospital<br>
<br>

<strong>Crew Challenge</strong><br>
Each crew pools its money for donation in friendly before regatta competition. This is a sporting way of putting the children first.<br>
<br>

<strong>Louie's Raffle &amp; Silent Auction</strong><br>
Buy raffle tickets on the day of the event for the chance to win a wide variety of great prizes.<br>
<br>

<strong>Louie's Challenge</strong><br>
Sanctioned bribery during the post-race party. This is an opportunity for boat crews, their shore teams and general party participants to buy their way to a better race position. All bribes given to Children's Hospital.<br>
<br>

<strong>Razoo (Online Fundraising Page for You or Your Boat)</strong><br>
Use Razoo to make direct donation(s) from boat crews, friends and family to Children's Hospital prior to and after the regatta. Boat crew members can go to the <a href="http://www.razoo.com/story/Louies17lastregatta">online donation site at Razoo</a>, find and/or register their boat and give a donation via their credit card.<br>
<br>

<strong>Give Raffle Items</strong><br>
Gifts can be given for the raffle held during the post-race party. To give items please contact us at <?php email("contactus@louislast.org"); ?>, with delivery by September 15th to 400 N. Executive Dr. Suite 100, Brookfield, WI 53005.<br>
