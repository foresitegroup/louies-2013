<h3>Louie's 17th Last Regatta</h3>

Louie's is one of the nation's largest fund raising regattas. It is a fun filled weekend of sailing and fund raising for Children's Hospital of Wisconsin. Held off the Milwaukee shoreline in the summer warmed waters of Lake Michigan, Louie's attracts hundreds of boats and thousands of sailors and fans to the Milwaukee sailing/fund raising scene.<br>
<br>

Louie's 17th Last Regatta will be held this year on September 26th. The skippers meeting and instructions are given on Friday the 25th at the Milwaukee Ale House. The race will take place on Lake Michigan at 12 noon Saturday the 26th with post race party starting at 3:00pm at the Milwaukee Ale House.<br>
<br>

This will be the third year that the Porker Run will be held.<br>
<br>

<strong>Louie's 17th Last Regatta highlights include:</strong><br>
<ul>
  <li>May 7th Launch Party</li>
  <li>Pre-race online fund raising by boat</li>
  <li>Captains Meeting Friday September 25th for sailors</li>
  <li>Louie's Last Regatta held on the famous beer can (shaped) course</li>
  <li>Post race dock side party</li>
  <li>Raffle</li>
  <li>Live Music</li>
  <li>Award Ceremony</li>
</ul>

